            <li>Bawang merah: 5 siung</li>
            <li>Bawang putih: 3 siung</li>
            <li>Jahe: 2 cm</li>
            <li>Kunyit: 2 cm</li>
            <li>Cabe merah besar: 3 buah</li>
            <li>Serai: 2 batang</li>
            <li>Daun jeruk: 4 lembar</li>
            <li>Daun salam: 2 lembar</li>
            <li>Garam dan gula merah: Secukupnya</li>
            <li>Minyak goreng: 2 sdm</li>
        </ul>
        <h3>Langkah-Langkah:</h3>
        <ol>
            <li>Haluskan bawang merah, bawang putih, jahe, kunyit, dan cabai menggunakan blender atau ulekan.</li>
            <li>Tumis bumbu halus bersama serai, daun jeruk, dan daun salam hingga harum.</li>
            <li>Masukkan daging sapi, aduk rata, lalu tuangkan santan.</li>
            <li>Masak dengan api kecil hingga daging empuk dan bumbu meresap. Tambahkan garam dan gula merah sesuai selera.</li>
            <li>Sajikan dengan nasi.</li>
