        <h3>Bahan-Bahan:</h3>
        <ul>
            <li>Daging sapi has dalam: 300 gram, iris tipis</li>
            <li>Bawang putih: 4 siung, cincang halus</li>
            <li>Gula pasir: 1 sdm</li>
            <li>Kecap asin: 2 sdm</li>
            <li>Minyak wijen: 1 sdm</li>
            <li>Perasan air jeruk nipis: 1 sdm</li>
            <li>Lada hitam: 1/2 sdt</li>
            <li>Daun bawang: 1 batang, iris halus</li>
            <li>Minyak goreng: 2 sdm</li>
        </ul>
        <h3>Langkah-Langkah:</h3>
        <ol>
            <li>Campurkan kecap asin, minyak wijen, gula pasir, perasan air jeruk nipis, bawang putih, lada hitam, dan daun bawang dalam mangkuk.</li>
            <li>Masukkan irisan daging sapi ke dalam campuran bumbu, diamkan selama 30 menit.</li>
            <li>Panaskan minyak dalam wajan, tumis daging hingga matang dan bumbu meresap.</li>
            <li>Sajikan dengan nasi putih.</li>
        </ol>
