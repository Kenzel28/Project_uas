        <h3>Bahan-Bahan:</h3>
        <ul>
            <li>Spaghetti: 200 gram</li>
            <li>Keju parmesan parut: 50 gram</li>
            <li>Kuning telur: 2 butir</li>
            <li>Krim masak: 100 ml</li>
            <li>Smoked beef: 100 gram, potong tipis</li>
            <li>Bawang putih: 2 siung, cincang halus</li>
            <li>Minyak zaitun: 2 sdm</li>
            <li>Garam: 1/2 sdt</li>
            <li>Merica hitam: 1/4 sdt</li>
        </ul>
        <h3>Langkah-Langkah:</h3>
        <ol>
            <li>Rebus pasta: Didihkan air dengan garam, masukkan spaghetti, masak selama 8-10 menit hingga al dente. Tiriskan.</li>
            <li>Siapkan saus: Campur kuning telur, keju parmesan, krim masak, garam, dan merica dalam mangkuk, aduk rata.</li>
            <li>Tumis bawang putih: Panaskan minyak zaitun, tumis bawang putih hingga harum. Tambahkan smoked beef, masak sebentar.</li>
            <li>Masukkan pasta: Tambahkan spaghetti yang sudah matang ke dalam wajan, aduk rata.</li>
            <li>Campur dengan saus: Matikan api, tuangkan campuran saus ke dalam wajan, aduk cepat agar saus melapisi pasta tanpa menggumpal.</li>
            <li>Sajikan: Taburi keju parmesan tambahan di atasnya jika diinginkan.</li>
        </ol>
