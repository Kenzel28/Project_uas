        <h3>Bahan-Bahan:</h3>
        <ul>
            <li>Jagung manis pipil: 200 gram</li>
            <li>Susu cair: 500 ml</li>
            <li>Bawang bombay: 50 gram, cincang halus</li>
            <li>Tepung terigu: 2 sdm</li>
            <li>Mentega: 2 sdm</li>
            <li>Kaldu ayam cair: 250 ml</li>
            <li>Garam: 1/2 sdt</li>
            <li>Merica bubuk: 1/4 sdt</li>
        </ul>
        <h3>Langkah-Langkah:</h3>
        <ol>
            <li>Siapkan bahan: Pipil jagung, lalu haluskan setengah bagiannya dengan blender menggunakan sedikit air.</li>
            <li>Tumis bawang bombay: Lelehkan mentega dalam panci, masukkan bawang bombay cincang, tumis hingga harum dan transparan.</li>
            <li>Tambahkan tepung: Masukkan tepung terigu, aduk cepat hingga tercampur rata dan membentuk pasta.</li>
            <li>Masak kuah: Tuangkan susu cair sedikit demi sedikit sambil terus diaduk agar tidak menggumpal.</li>
            <li>Tambahkan jagung: Masukkan jagung yang dihaluskan dan yang utuh ke dalam panci.</li>
            <li>Tambahkan kaldu: Tuang kaldu ayam, garam, dan merica. Aduk hingga sup mendidih dan mengental.</li>
            <li>Finishing: Masak hingga tekstur creamy dan jagung matang sempurna. Angkat dan sajikan hangat.</li>

        </ol>
