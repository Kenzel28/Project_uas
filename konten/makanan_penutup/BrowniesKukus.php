        <h3>Bahan-Bahan:</h3>
        <ul>
            <li>Tepung terigu protein sedang: 100 gram</li>
            <li>Cokelat bubuk: 30 gram</li>
            <li>Gula pasir: 150 gram</li>
            <li>Telur: 4 butir</li>
            <li>Margarin: 100 gram, lelehkan</li>
            <li>Dark chocolate: 100 gram, lelehkan</li>
            <li>Baking powder: 1/2 sdt</li>
            <li>Vanili bubuk: 1/4 sdt</li>
        </ul>
        <h3>Langkah-Langkah:</h3>
        <ol>
            <li>Siapkan kukusan: Panaskan kukusan terlebih dahulu, lapisi tutupnya dengan kain agar air tidak menetes.</li>
            <li>Mixer telur dan gula: Kocok telur dan gula hingga mengembang dan berwarna putih pucat.</li>
            <li>Campur bahan kering: Ayak tepung terigu, cokelat bubuk, baking powder, dan vanili, lalu masukkan perlahan ke dalam adonan telur. Aduk rata dengan spatula.</li>
            <li>Tambahkan cokelat: Tuangkan margarin leleh dan dark chocolate yang sudah dilelehkan, aduk perlahan hingga tercampur rata.</li>
            <li>Tuang ke loyang: Oles loyang dengan margarin, alasi dengan kertas roti, lalu tuang adonan brownies ke dalamnya.</li>
            <li>Kukus: Masukkan loyang ke dalam kukusan, masak selama 30-40 menit hingga matang. Lakukan tes tusuk untuk memastikan kematangan.</li>
            <li>Sajikan: Dinginkan terlebih dahulu sebelum dipotong-potong.</li>

        </ol>
